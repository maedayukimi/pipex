#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	*s;
	int		len;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	s = ft_itoa(n);
	if (s != NULL)
	{
		len = ft_strlen(s);
		write (fd, s, len);
		free(s);
	}
}
